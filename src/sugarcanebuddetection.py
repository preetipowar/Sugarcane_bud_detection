def start_webcam_with_display():
    """Start webcam with live video display"""
    js = Javascript('''
    async function startVideoStream() {
      const div = document.createElement('div');
      div.style.textAlign = 'center';
      document.body.appendChild(div);

      // Create video element (hidden - we'll use canvas)
      const video = document.createElement('video');
      video.style.display = 'none';
      video.width = 640;
      video.height = 480;

      // Create canvas for display
      const canvas = document.createElement('canvas');
      canvas.width = 640;
      canvas.height = 480;
      canvas.style.border = '2px solid #00ff00';
      canvas.style.borderRadius = '10px';
      div.appendChild(canvas);

      // Add detection counter display
      const counterDiv = document.createElement('div');
      counterDiv.id = 'detectionCounter';
      counterDiv.style.fontSize = '24px';
      counterDiv.style.fontWeight = 'bold';
      counterDiv.style.color = '#00ff00';
      counterDiv.style.marginTop = '10px';
      counterDiv.innerHTML = 'Detections: 0 | Status: Waiting...';
      div.appendChild(counterDiv);

      // Get webcam stream
      const stream = await navigator.mediaDevices.getUserMedia({
        video: {width: 640, height: 480}
      });

      video.srcObject = stream;
      await video.play();

      const ctx = canvas.getContext('2d');

      // Store globally
      window.videoElement = video;
      window.canvasElement = canvas;
      window.ctxElement = ctx;
      window.streamActive = true;

      google.colab.output.setIframeHeight(document.documentElement.scrollHeight, true);

      return "Stream started";
    }
    ''')
    display(js)
    return eval_js('startVideoStream()')

def get_frame():
    """Get current frame from webcam"""
    js = Javascript('''
    async function captureFrame() {
      if (!window.streamActive) return null;

      const video = window.videoElement;
      const canvas = document.createElement('canvas');
      canvas.width = video.videoWidth;
      canvas.height = video.videoHeight;
      const ctx = canvas.getContext('2d');
      ctx.drawImage(video, 0, 0);
      return canvas.toDataURL('image/jpeg', 0.8);
    }
    ''')

    try:
        data = eval_js('captureFrame()')
        if data:
            binary = b64decode(data.split(',')[1])
            img = Image.open(io.BytesIO(binary))
            return cv2.cvtColor(np.array(img), cv2.COLOR_RGB2BGR)
    except:
        pass
    return None

def display_frame(img_array, num_detections, fps):
    """Display processed frame with detections on canvas"""
    # Encode image
    _, buffer = cv2.imencode('.jpg', img_array)
    img_base64 = b64encode(buffer).decode('utf-8')

    # Update canvas and counter
    js = Javascript(f'''
    async function updateDisplay() {{
      const img = new Image();
      img.src = 'data:image/jpeg;base64,{img_base64}';
      await img.decode();

      const ctx = window.ctxElement;
      ctx.drawImage(img, 0, 0, window.canvasElement.width, window.canvasElement.height);

      // Update counter
      const counter = document.getElementById('detectionCounter');
      if (counter) {{
        const status = {num_detections} > 0 ? '🟢 BUD DETECTED!' : '⚪ Scanning...';
        counter.innerHTML = `Detections: {num_detections} | FPS: {fps:.1f} | ${{status}}`;
        counter.style.color = {num_detections} > 0 ? '#ff0000' : '#00ff00';
      }}
    }}
    updateDisplay();
    ''')
    display(js)

def send_arduino_signal(num_buds):
    """Send signal to Arduino when bud is detected"""
    if ARDUINO_CONNECTED and arduino:
        try:
            # Send '1' when bud detected, '0' when no bud
            signal = '1' if num_buds > 0 else '0'
            arduino.write(signal.encode())

            # Read response from Arduino (optional)
            if arduino.in_waiting > 0:
                response = arduino.readline().decode('utf-8').strip()
                return response
        except Exception as e:
            print(f"Arduino error: {e}")
    return None

