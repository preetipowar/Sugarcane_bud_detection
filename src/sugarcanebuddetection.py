# Detection loop
try:
    frame_count = 0
    detection_count = 0
    start_time = time.time()
    last_detection_state = False

    while True:
        # Get frame
        frame = get_frame()

        if frame is not None:
            # Run detection
            results = model(frame, conf=0.5, verbose=False)

            # Count detections
            num_buds = len(results[0].boxes)
            detection_count += num_buds

            # Draw bounding boxes (RED boxes for detected buds)
            annotated_frame = results[0].plot(
                line_width=3,
                font_size=1.5,
                labels=True,
                conf=True
            )

            # Calculate FPS
            frame_count += 1
            elapsed_time = time.time() - start_time
            fps = frame_count / elapsed_time if elapsed_time > 0 else 0

            # Display frame with detection info
            display_frame(annotated_frame, num_buds, fps)

            # Send signal to Arduino when bud detected
            if num_buds > 0:
                if not last_detection_state:  # Only send on new detection
                    arduino_response = send_arduino_signal(num_buds)
                    print(f"🔴 BUD DETECTED! Count: {num_buds}")
                    if arduino_response:

                        print(f"   Arduino: {arduino_response}")
                last_detection_state = True
            else:
                if last_detection_state:  # Send clear signal
                    send_arduino_signal(0)
                last_detection_state = False

            # Progress update every 30 frames
            if frame_count % 30 == 0:
                print(f"✓ Frames: {frame_count} | Detections: {detection_count} | FPS: {fps:.1f}")

        time.sleep(0.05)  # 20 FPS target

except KeyboardInterrupt:
    print("\n✓ Detection stopped")
    print(f"Total frames: {frame_count}")
    print(f"Total detections: {detection_count}")
    if ARDUINO_CONNECTED:
        arduino.close()
        print("✓ Arduino connection closed")

